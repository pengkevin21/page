public class App4_11
{
	public static void main(String args[])
	{
		//畫面輸出
		System.out.println("「<<」實作:");
		System.out.println("5 << 2 = " + (5 << 2));
		System.out.println("「>>>」實作:");
		System.out.println("7 >>> 1 = " + (7 >>> 1));
		System.out.println("「&」實作:");
		System.out.println("9 & 16 = " + (9 & 16));
		System.out.println("「~」實作:");
		System.out.println("~11 = " + ~11);
	}
}
